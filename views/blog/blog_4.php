                <p>You need to ensure to continue taking care of your health on your way back from the treatment abroad. You have spent your hard-earned money on medical tourism. Thus, remember to take precautions seriously, as you check your return flight back home. There are a few preventive measures to look out for after the surgery in Turkey or Mexico.</p>
                <h6>Know All About Medical Emergency Kit on Board</h6>
                <p>Modern commercial airlines have external defibrillators onboard. This said, you can expect the airline attendants to be CPR trained and certified. Thus, as you step aboard the flight, you can relax and rest assured you are in safe hands as you fly back home.</p>
                <h6>Trapped Gas during Flight</h6>
                <p>A patient traveling soon after the operation might experience discomfort in many aspects. Since the air pressure is prominent even inside the flight, and as you are sitting or reclining, the discomfort is evident. The trapped gas in the body seems to expand and cause pain during the takeoff or turbulence. The pain is common even among the regular flyers who chew gum or put buds to reduce the impact. A patient might not be able to explain why, but they too experience this numbing pain during the gas pressure in the middle ear, intestine, or chest. Let the airline attendant know of the problem in case you cannot stand it right away.</p>
                <h6>Blood Clot Issues</h6>
                <p>Long flight back home may be the most cumbersome things to happen. If you are operated in the abdominal or lower abdominal region, sitting for long may be very disturbing. You may feel legs stiffen up, or blood clotting in specific areas in the lower part of the body. If you are suffering from Deep Vein Thrombosis, the long hours of flight after your surgeries can be painful. Should these clots travel to the area near the lungs, they can be deadly. So, raising an alarm is essential.</p>
                <h6>Check for First Aid and Emergency Services</h6>
                <p>Take note of all the emergency phone numbers before getting in the flight. There are even ambulance services on offer so, coordinate with the companies like Meddistant for immediate support.</p>
