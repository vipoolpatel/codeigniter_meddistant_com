			  <p class="text-gray-base">You may benefit from both forms of injectable treatment, but a consultation with Dr Hobson will confirm the cause of your facial aging and find the right regimen for you. If you have wrinkles that appear whenever you use muscles on your forehead or around your eyes, you may benefit from BOTOX or Xeomin. Otherwise, your nasolabial folds, marionette lines and overall volume loss will respond better to dermal fillers.</p>
