              <h5>Types of knee surgery</h5>
              <p>There are various types of knee surgery. The type that a person has will depend on the particular injury to the knee joint. These include:</p>
              <h6>Meniscectomy</h6>
              The official name of the surgery that involves the removal of a portion of the meniscus cartilage from the knee joint. The meniscus is a shock-absorbing wedge of cartilage that sits between the bone ends to provide cushioning and support. Smaller meniscus tears can usually be trimmed to relieve the symptoms of a torn meniscus.<p>
              <h6>Total knee replacement</h6>
              <p>When other treatments cannot alleviate the pain, total knee replacement is the most common kind of knee surgery for arthritis. The operation involves replacing the whole joint. Surgeons only undertake total knee replacement surgery as a last resort, often when the joint is irreparable, the damage and reduced mobility interferes with everyday life.</p>
              <p>During the procedure, a surgeon will remove the joint and replace it with one typically made from Oxinium oxidized zirconium a metal alloy with a surface of zirconia ceramic for the weight bearing surface.</p>
              <h6>Arthroscopy</h6>
              <p>Arthroscopy is a minimally invasive surgery that involves making a small cut in the skin over the knee, inserting a small light, and using small instruments to diagnose and treat knee problems, including:</p>
              <ul class="list-marked">
                <li>Removing or repairing torn menisci, which are part of the cartilage in the knee joint</li>
                <li>Repairing a torn cruciate ligament inside the knee</li>
                <li>Trimming torn pieces of joint cartilage</li>
                <li>Removing loose bodies, which are small fragments of cartilage or bone floating inside the knee</li>
                <li>Removing an inflamed joint lining</li>
              </ul>
               <h6>Arthroscopy</h6>
               <p>This is an operation that involves the surgeon cutting, reshaping, and repositioning the bones to take some weight off the damaged part of the knee.</p>
               <p>Sometimes, surgeons carry out this procedure to help correct a broken bone that has not healed properly.</p>
